    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        //Capturamos
        String nombre = txtNombre.getText().trim();
        String detalle = txtAreaDetalle.getText().trim();
        boolean isObligatorio = false;

        //Validamos el check
        if (cheBoxObligatorio.isSelected()) {
            isObligatorio = true;
        } else {
            isObligatorio = false;
        }

        if (!nombre.isEmpty()) {
            if (sc.verificarLongitudServicio(nombre)) {
                if (!detalle.isEmpty()) {
                    //Guardamos
                    sc.guardarServicio(nombre, detalle, isObligatorio);
                    //Limpiamos
                    sc.limpiarCampos(txtNombre, txtAreaDetalle, cheBoxObligatorio);
                } else {
                    JOptionPane.showMessageDialog(null, "Ingrese el detalle");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Ingrese el nombre");
        }

    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //Capturamos
        String idDoc = txtId.getText().trim();

        //Eliminamos
        if (!idDoc.isEmpty()) {
            sc.eliminarServicio(idDoc);
            //Limpiamos
            sc.limpiarCampos(txtNombre, txtAreaDetalle, cheBoxObligatorio);
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un ID de un dato de la tabla");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        //Capturamos
        String idDoc = txtId.getText().trim();
        String nombre = txtNombre.getText().trim();
        String detalle = txtAreaDetalle.getText().trim();
        boolean isObligatorio = false;

        //Validamos el check
        if (cheBoxObligatorio.isSelected()) {
            isObligatorio = true;
        } else {
            isObligatorio = false;
        }

        if (!idDoc.isEmpty()) {
            if (!nombre.isEmpty()) {
                if (sc.verificarLongitudServicio(nombre)) {
                    if (!detalle.isEmpty()) {
                        //Actualizamos
                        sc.actualizarServicio(idDoc, nombre, detalle, isObligatorio);
                    } else {
                        JOptionPane.showMessageDialog(null, "Ingrese el detalle");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese el nombre");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un ID de un dato de la tabla");
        }


    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimpiarActionPerformed
        sc.limpiarCampos(txtNombre, txtAreaDetalle, cheBoxObligatorio);
    }//GEN-LAST:event_btnLimpiarActionPerformed

    private void tblServicioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblServicioMouseClicked
        int selection = tblServicio.getSelectedRow();
        txtId.setText(tblServicio.getValueAt(selection, 0) + "");
        txtNombre.setText(tblServicio.getValueAt(selection, 1) + "");
        txtAreaDetalle.setText(tblServicio.getValueAt(selection, 2) + "");
        String isObligatorio = tblServicio.getValueAt(selection, 3) + "";
        if (isObligatorio.equals("Si")) {
            cheBoxObligatorio.setSelected(true);
        } else if (isObligatorio.equals("No")) {
            cheBoxObligatorio.setSelected(false);
        }

    }//GEN-LAST:event_tblServicioMouseClicked

