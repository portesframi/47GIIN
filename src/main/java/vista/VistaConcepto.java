    private void cheBoxEsComMulActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cheBoxEsComMulActionPerformed
        if (cheBoxEsComMul.isSelected()) {
            txtNombreCS.setEnabled(false);
            txtNombreCS.setText("");
            txtAreaConceptoMultiple.setEnabled(true);
            txtNombreCM.setEnabled(true);

        } else if (!cheBoxEsComMul.isSelected()) {
            txtNombreCS.setEnabled(true);
            txtAreaConceptoMultiple.setEnabled(false);
            txtAreaConceptoMultiple.setText("");
            txtNombreCM.setEnabled(false);
            txtNombreCM.setText("");
        }
    }//GEN-LAST:event_cheBoxEsComMulActionPerformed

