    private void tblConceptoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblConceptoMouseClicked
        int selection = tblConcepto.getSelectedRow();
        String val = tblConcepto.getValueAt(selection, 3) + "";
        if (val.equals("Si")) {
            cheBoxEsComMul.setSelected(true);
            txtId.setText(tblConcepto.getValueAt(selection, 0) + "");
            txtNombreCM.setEnabled(true);
            txtAreaConceptoMultiple.setEnabled(true);
            txtNombreCS.setEnabled(false);
            txtNombreCS.setText("");
            txtNombreCM.setText(tblConcepto.getValueAt(selection, 1) + "");
            txtAreaConceptoMultiple.setText(tblConcepto.getValueAt(selection, 2).toString().replace(",", ";") + "");
        } else {
            cheBoxEsComMul.setSelected(false);
            txtId.setText(tblConcepto.getValueAt(selection, 0) + "");
            txtNombreCM.setEnabled(false);
            txtAreaConceptoMultiple.setEnabled(false);
            txtNombreCS.setEnabled(true);
            txtNombreCM.setText("");
            txtAreaConceptoMultiple.setText("");
            txtId.setText(tblConcepto.getValueAt(selection, 0) + "");
            txtNombreCS.setText(tblConcepto.getValueAt(selection, 1) + "");
        }
    }//GEN-LAST:event_tblConceptoMouseClicked

    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        //Capturamos
        String nombreCS = txtNombreCS.getText().trim();
        String nombreCM = txtNombreCM.getText().trim();

        boolean esConceptoMultiple = false;
        String conceptosString = txtAreaConceptoMultiple.getText();
        String[] elements = conceptosString.split(";");
        // Crear un ArrayList
        ArrayList<String> arrayConceptos = new ArrayList<>();

        if (!nombreCM.isEmpty()) {
            if (cheBoxEsComMul.isSelected()) {
                esConceptoMultiple = true;
                if (!conceptosString.isEmpty()) {
                    // Agregar cada elemento al ArrayList
                    for (String element : elements) {
                        arrayConceptos.add(element.trim()); // Trim para eliminar espacios en blanco al principio o al final
                    }
//                System.out.println("Multiple");
                    cc.guardarConcepto(nombreCS, nombreCM, esConceptoMultiple, arrayConceptos);
                    cc.limpiarCampos(txtId, txtNombreCS, txtNombreCM, txtAreaConceptoMultiple, cheBoxEsComMul);
                } else {
                    JOptionPane.showMessageDialog(null, "Ingrese el o los conceptos separados por ;");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese el nombre en concepto multiple");
            }

        } else {
            esConceptoMultiple = false;
            if (!nombreCS.isEmpty()) {
//                System.out.println("Simple");
                //Guardamos
                cc.guardarConcepto(nombreCS, nombreCM, esConceptoMultiple, arrayConceptos);
                //Limpiamos
                cc.limpiarCampos(txtId, txtNombreCS, txtNombreCM, txtAreaConceptoMultiple, cheBoxEsComMul);
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese el nombre en concepto simple");
            }
        }

    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        //Capturamos
        String idDoc = txtId.getText().trim();
        boolean esConceptoMultiple = false;

        if (esConceptoMultiple) {
            esConceptoMultiple = true;
            if (!idDoc.isEmpty()) {
                cc.eliminarConcepto(idDoc, esConceptoMultiple);

                //Limpiamos
                cc.limpiarCampos(txtId, txtNombreCS, txtNombreCM, txtAreaConceptoMultiple, cheBoxEsComMul);
            } else {
                JOptionPane.showMessageDialog(null, "Seleccione un ID de un dato de la tabla");
            }
        } else {
            esConceptoMultiple = false;
            if (!idDoc.isEmpty()) {
                cc.eliminarConcepto(idDoc, esConceptoMultiple);

                //Limpiamos
                cc.limpiarCampos(txtId, txtNombreCS, txtNombreCM, txtAreaConceptoMultiple, cheBoxEsComMul);
            } else {
                JOptionPane.showMessageDialog(null, "Seleccione un ID de un dato de la tabla");
            }
        }

        //Eliminamos

    }//GEN-LAST:event_btnEliminarActionPerformed

